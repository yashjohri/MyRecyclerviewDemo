package com.example.myrecyclerviewdemo;

public class mCategoryData {

    private String t;
    private String pF;

    public mCategoryData() {
    }

    public mCategoryData(String t, String pF) {
        this.t = t;
        this.pF = pF;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    public String getpF() {
        return pF;
    }

    public void setpF(String pF) {
        this.pF = pF;
    }
}
